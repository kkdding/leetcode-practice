func mergeTwoLists(list1 *ListNode, list2 *ListNode) *ListNode {
     dummy := new(ListNode)
     ptr := dummy
     ptr1, ptr2 := list1, list2
     for ptr1 != nil && ptr2 != nil {
        if ptr1.Val > ptr2.Val {
            ptr.Next = ptr2
            ptr2 = ptr2.Next
        } else {
            ptr.Next = ptr1
            ptr1 = ptr1.Next
        }
        ptr = ptr.Next
     }
     if ptr1 == nil {
         ptr.Next = ptr2
     } else {
         ptr.Next = ptr1
     }
     return dummy.Next
}

