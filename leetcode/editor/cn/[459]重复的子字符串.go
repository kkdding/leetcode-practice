package main

func repeatedSubstringPattern(s string) bool {
    for i := 0; i < len(s)/2; i++ {
        sub := s[:i+1]
        if len(s)%len(sub) != 0 {
            continue
        }
        n := len(s) / len(sub)
        for n > 1 {
            sub += sub
            n--
        }
        if sub == s {
            return true
        }
    }
    return false
}

